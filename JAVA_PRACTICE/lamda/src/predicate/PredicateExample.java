package predicate;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.List;
import java.util.function.Predicate;

public class PredicateExample {
	private static List<Student> list = Arrays.asList(
				new Student("홍길동", "남자", 90),
				new Student("김순희", "여자", 90),
				new Student("김자바", "남자", 95),
				new Student("박한나", "여자", 92)
	);
	
	public static double avg(Predicate<Student> predicate) {
		int count = 0, sum = 0;
		
		for(Student student :list) {
			if(predicate.test(student)) {
				count++;
				sum += student.getScore();
			}
		}
		return (double) sum / count;
	}
	
	public static List<Student> getStudents(Predicate<Student> predicate) {
		List<Student> tempList = new ArrayList<>();
		
//		for(Student student : list) {
//			if(predicate.test(student)) {
//				tempList.add(student);
//			}
//		}
		
		list.forEach(e->{
			if(predicate.test(e) ) {
				tempList.add(e);
			}
		});
		
		return tempList;
	}
	
	public static void main(String[] args) {
		double maleAvg = avg(t -> t.getSex().equals("남자"));
		System.out.println("남자 평균 점수 : " + maleAvg);
		
		double femaleAvg = avg(t -> t.getSex().equals("여자"));
		System.out.println("여자 평균 점수 : " + femaleAvg);
		
		List<Student> slist = getStudents(t -> t.getSex().equals("남자"));
		slist.forEach(t -> System.out.println(t));
		
		getStudents(t -> t.getScore() >= 95).forEach(t -> System.out.println(t));
		System.out.println();
		
		getStudents(t -> (t.getScore() >= 95 && t.getSex().equals("남자"))).forEach(t -> System.out.println(t));
	}
}
